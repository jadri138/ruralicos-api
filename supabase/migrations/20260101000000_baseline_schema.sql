create or replace function public.buscar_alertas_por_embedding_mia(p_query_embedding vector, p_match_count integer default 40, p_min_similarity double precision default 0.18)
 returns table(id bigint, titulo text, resumen text, resumen_final text, url text, fecha text, region text, fuente text, provincias jsonb, sectores jsonb, subsectores jsonb, tipos_alerta jsonb, estado_ia text, duplicado_de bigint, created_at timestamp with time zone, similitud double precision)
 language sql
 stable
 set search_path to 'public', 'pg_catalog', 'pg_temp'
as $function$
  select
    a.id,
    a.titulo,
    a.resumen,
    a.resumen_final,
    a.url,
    a.fecha,
    a.region,
    a.fuente,
    a.provincias,
    a.sectores,
    a.subsectores,
    a.tipos_alerta,
    a.estado_ia,
    a.duplicado_de,
    a.created_at,
    (1 - (a.embedding <=> p_query_embedding))::double precision as similitud
  from public.alertas a
  where a.embedding is not null
    and coalesce(a.estado_ia, '') = 'listo'
    and a.duplicado_de is null
    and (1 - (a.embedding <=> p_query_embedding)) >= p_min_similarity
  order by a.embedding <=> p_query_embedding asc, a.created_at desc
  limit greatest(1, least(coalesce(p_match_count, 40), 100));
$function$;

create or replace function public.buscar_alertas_similares(p_perfil_vector vector, p_fecha text, p_limite integer default 10)
 returns table(id bigint, titulo text, resumen_final text, url text, sectores jsonb, subsectores jsonb, tipos_alerta jsonb, provincias jsonb, fuente text, similitud double precision)
 language sql
 stable
 set search_path to 'public', 'pg_catalog', 'pg_temp'
as $function$
  select
    a.id,
    a.titulo,
    a.resumen_final,
    a.url,
    a.sectores,
    a.subsectores,
    a.tipos_alerta,
    a.provincias,
    a.fuente,
    1 - (a.embedding <=> p_perfil_vector) as similitud
  from public.alertas a
  where a.fecha = p_fecha
    and a.estado_ia = 'listo'
    and a.embedding is not null
    and a.duplicado_de is null
  order by a.embedding <=> p_perfil_vector
  limit greatest(1, least(coalesce(p_limite, 10), 50));
$function$;

create or replace function public.buscar_mia_knowledge_chunks_por_embedding(p_query_embedding vector, p_match_count integer default 8, p_min_similarity double precision default 0.18, p_organization_id bigint default null::bigint)
 returns table(id bigint, document_id bigint, titulo text, resumen text, snippet text, fuente text, fuente_tipo text, categoria text, url text, fecha date, organization_id bigint, similitud double precision, chunk_index integer, metadata_json jsonb)
 language sql
 stable
 set search_path to 'public', 'pg_catalog', 'pg_temp'
as $function$
  select
    c.id,
    d.id as document_id,
    coalesce(c.titulo, d.titulo) as titulo,
    left(c.contenido, 1200) as resumen,
    left(c.contenido, 520) as snippet,
    d.fuente,
    d.fuente_tipo,
    d.categoria,
    d.url,
    d.fecha_documento as fecha,
    coalesce(c.organization_id, d.organization_id) as organization_id,
    1 - (c.embedding <=> p_query_embedding) as similitud,
    c.chunk_index,
    c.metadata_json
  from public.mia_knowledge_chunks c
  join public.mia_knowledge_documents d on d.id = c.document_id
  where
    d.status = 'active'
    and c.embedding is not null
    and (
      (
        p_organization_id is null
        and coalesce(c.organization_id, d.organization_id) is null
      )
      or
      (
        p_organization_id is not null
        and (
          coalesce(c.organization_id, d.organization_id) is null
          or coalesce(c.organization_id, d.organization_id) = p_organization_id
        )
      )
    )
    and 1 - (c.embedding <=> p_query_embedding) >= p_min_similarity
  order by c.embedding <=> p_query_embedding
  limit greatest(1, least(50, coalesce(p_match_count, 8)));
$function$;

